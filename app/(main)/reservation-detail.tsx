  const { data: allReservationsRaw = [], isLoading } = useQuery({
    queryKey: ["reservations"],
    queryFn: reservationsApi.getAll,
  });

  const { data: allServicesRaw = [] } = useQuery({
    queryKey: ["services"],
    queryFn: servicesApi.getAll,
    retry: 1,
  });

